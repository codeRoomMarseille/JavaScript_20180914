// *************************************
// Converti un nombre de seconde en jour
// @Param le nombre de seconde
// @Return le nombre de jour
// *************************************
function secondeToJour(nbSeconde) {
    return nbSeconde / 86400;
}

// *********************************
// Converti des metres en kilometres
// @Param le nombre de metres
// @Return le nombre de kilometres
// *********************************
function metreToKilometre(nbMetre) {
    return nbMetre / 1000;
}

// ***********************************************
// Converti des metres carrÃ©s en kilometres carres
// @Param le nombre de metres carres
// @Return le nombre de kilometres carres
// ***********************************************
function metreCarreToKilometreCarre(nbMetreCarre) {
    return nbMetreCarre / 1000000;
}

// *********************************************
// Converti des metres cubes en kilometres cubes
// @Param le nombre de metres cubes
// @Return le nombre de kilometres cubes
// *********************************************
function metreCubeToKilometreCube(nbMetreCube) {
    return nbMetreCube / 1000000000;
}
